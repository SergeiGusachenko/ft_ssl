#include "libft.h"

char	*fill_str(char *str, int nb, int i)
{
	if (i == 1)
		str[0] = '-';
	if (nb == 0)
	{
		str[i] = '0';
		i++;
	}
	while (nb != 0)
	{
		str[i] = (nb % 10) + '0';
		nb = nb / 10;
		i++;
	}
	str[i] = '\0';
	return (str);
}
